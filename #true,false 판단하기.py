#if판단문
a = 3
#변수 a가 3임
if a == 2:  #a가 2라면
    print("A") 
    #A를 출력해줘! 근데 a가 3이기 때문에 false라서 출력 안함!

if a == 3:
    print("B")
    #그러면 a가 3일때, B를 출력해줘!
    #true이기 때문에 실행됨!

#if-else 판단문
if a == 4:
    print("C")
    #a가 4일 때 C를 출력해줘, 아니면?
else:
    print("D")
    #4가 아닐경우 D를 출력해줘
